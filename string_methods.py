course = 'python for beginners '
# print no of character
#here len is general purpose function
print(len(course))
#in uppercase 
print(course.upper())
print(course)
#in lowercase 
print(course.lower)
#find char 
print(course.find('o'))
#find a whole word 
print(course.find('beginner'))
# prints 11 because beginner starts at index 11

#repacing char ie : replace beginner with intermediate
print(course.replace('beginner', "intermediate"))

#to check word or char in a variable(course)
print('python'in course)
# in operator gives bool value

